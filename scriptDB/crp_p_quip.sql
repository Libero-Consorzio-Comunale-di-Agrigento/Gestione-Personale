CREATE OR REPLACE PACKAGE BODY p_quip IS

   FUNCTION VERSIONE  RETURN VARCHAR2 IS
   BEGIN
   RETURN 'V1.0 del 20/01/2003';
   END VERSIONE;

PROCEDURE main(prenotazione IN NUMBER, passo IN NUMBER) IS
	p_pagina	number := 1;
	contarighe  number;
procedure insert_intestazione( contarighe in out number, pagina in number) is
	begin
		contarighe:=contarighe+1;
		insert into a_appoggio_stampe
		 values ( prenotazione
        		, passo
		        , pagina
	        	, contarighe
	           	, null
	           )
			   ;
		contarighe:=contarighe+1;
		insert into a_appoggio_stampe
		 values ( prenotazione
        		, passo
		        , pagina
	        	, contarighe
	           	, lpad('VERIFICA INTEGRITA` REFERENZIALE QUALIFICHE INCENTIVO',(132-length('VERIFICA INTEGRITA` REFERENZIALE QUALIFICHE INCENTIVO'))/2+(length('VERIFICA INTEGRITA` REFERENZIALE QUALIFICHE INCENTIVO')))
	           )
			   ;
		contarighe:=contarighe+1;
		insert into a_appoggio_stampe
		 values ( prenotazione
        		, passo
		        , pagina
	        	, contarighe
	           	, null
	           )
			   ;
		contarighe:=contarighe+1;
		insert into a_appoggio_stampe
		 values ( prenotazione
        		, passo
		        , pagina
	        	, contarighe
	           	, 'Qual.  Dal        Gruppo Tabella'
	           )
			   ;
		contarighe:=contarighe+1;
		insert into a_appoggio_stampe
		 values ( prenotazione
        		, passo
		        , P_pagina
	        	, contarighe
	           	, '------ ---------- ------ --------------------------'
	           )
			   ;
		commit;
		end;
BEGIN
	select nvl(max(riga),0)
	  into contarighe
	  from a_appoggio_stampe
	 where no_prenotazione = prenotazione
	 ;
	for cur_riga in (select  quip.numero                            qualifica
       ,substr(to_char(quip.dal,'dd/mm/yyyy'),1,10)
                                               dal
       ,null                                   gruppo
       ,'QUALIFICHE'                           tabella
  from  qualifiche_incentivo quip
 where  not exists
        (select 1 from qualifiche qual
          where quip.numero = qual.numero
        )
union
select  quip.numero                            qualifica
       ,substr(to_char(quip.dal,'dd/mm/yyyy'),1,10)
                                               dal
       ,quip.gruppo                            gruppo
       ,'GRUPPI_INCENTIVO'                     tabella
  from  qualifiche_incentivo quip
 where  not exists
        (select 1 from gruppi_incentivo grip
          where grip.codice = quip.gruppo
        )
order   by 4,1,2


) loop
		if mod(contarighe,57)= 0 then
			   insert_intestazione(contarighe,p_pagina);
		end if;
		contarighe:=contarighe+1;
		insert into a_appoggio_stampe
		 values ( prenotazione
        		, passo
		        , P_pagina
		       	, contarighe
		       	, rpad(nvl(to_char(cur_riga.qualifica),' '),6)||' '||
				  rpad(nvl(cur_riga.dal,' '),10)||' '||
				  rpad(nvl(cur_riga.gruppo,' '),6)||' '||
				  cur_riga.tabella
		        );
	end loop;
	while TRUE loop
		if mod(contarighe,57)= 0 then exit; end if;
		contarighe := contarighe+1;
		insert into a_appoggio_stampe
		values ( prenotazione
        		, passo
		        , P_pagina
		       	, contarighe
		       	, null
		        );
	end loop;
	COMMIT;
END MAIN;
END p_quip;
/

