CREATE OR REPLACE PACKAGE BODY p_apip IS

   FUNCTION VERSIONE  RETURN VARCHAR2 IS
   BEGIN
   RETURN 'V1.0 del 20/01/2003';
   END VERSIONE;

PROCEDURE main(prenotazione IN NUMBER, passo IN NUMBER) IS
	p_pagina	number := 1;
	contarighe  number;
procedure insert_intestazione( contarighe in out number, pagina in number) is
	begin
		contarighe:=contarighe+1;
		insert into a_appoggio_stampe
		 values ( prenotazione
        		, passo
		        , pagina
	        	, contarighe
	           	, null
	           )
			   ;
		contarighe:=contarighe+1;
		insert into a_appoggio_stampe
		 values ( prenotazione
        		, passo
		        , pagina
	        	, contarighe
	           	, lpad('VERIFICA INTEGRITA` REFERENZIALE APPLICAZIONI INCENTIVO',(132-length('VERIFICA INTEGRITA` REFERENZIALE APPLICAZIONI INCENTIVO'))/2+(length('VERIFICA INTEGRITA` REFERENZIALE APPLICAZIONI INCENTIVO')))
	           )
			   ;
		contarighe:=contarighe+1;
		insert into a_appoggio_stampe
		 values ( prenotazione
        		, passo
		        , pagina
	        	, contarighe
	           	, null
	           )
			   ;
		contarighe:=contarighe+1;
		insert into a_appoggio_stampe
		 values ( prenotazione
        		, passo
		        , pagina
	        	, contarighe
	           	, 'Progetto App. Parte Tabella'
	           )
			   ;
		contarighe:=contarighe+1;
		insert into a_appoggio_stampe
		 values ( prenotazione
        		, passo
		        , P_pagina
	        	, contarighe
	           	, '-------- ---- ------ -------------------------------'
	           )
			   ;
		commit;
		end;
BEGIN
	select nvl(max(riga),0)
	  into contarighe
	  from a_appoggio_stampe
	 where no_prenotazione = prenotazione
	 ;
	for cur_riga in (select  apip.progetto                          progetto
       ,apip.scp                               scp
       ,null                                   parte
       ,'PROGETTI INCENTIVO'                   tabella
  from  applicazioni_incentivo apip 
 where  not exists
       (select 1 
          from progetti_incentivo prip
         where prip.progetto = apip.progetto
       )
union
select  apip.progetto                          progetto
       ,apip.scp                               scp
       ,apip.parte                             parte
       ,'PARTI APPLICATIVE'                    tabella
  from  applicazioni_incentivo apip 
 where  not exists
       (select 1 
          from parti_applicative paap
         where paap.codice = apip.parte
       )
 order by 4,1,2

) loop
		if mod(contarighe,57)= 0 then
			   insert_intestazione(contarighe,p_pagina);
		end if;
		contarighe:=contarighe+1;
		insert into a_appoggio_stampe
		 values ( prenotazione
        		, passo
		        , P_pagina
		       	, contarighe
		       	, rpad(nvl(cur_riga.progetto,' '),8)||' '||
				  rpad(nvl(cur_riga.scp,' '),4)||' '||
				  rpad(nvl(cur_riga.parte,' '),5)||' '||
				  cur_riga.tabella
		        );
	end loop;
	while TRUE loop
		if mod(contarighe,57)= 0 then exit; end if;
		contarighe := contarighe+1;
		insert into a_appoggio_stampe
		values ( prenotazione
        		, passo
		        , P_pagina
		       	, contarighe
		       	, null
		        );
	end loop;
	COMMIT;
END MAIN;
END p_apip;
/

