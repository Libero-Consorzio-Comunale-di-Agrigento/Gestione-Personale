CREATE TABLE DENUNCIA_GLA(
ANNO              NUMBER(4)   NOT NULL,
GESTIONE          VARCHAR2(8) NOT NULL,
CI                NUMBER(8)   NOT NULL,
ATTIVITA          VARCHAR2(4) ,
ECCEDENZA_VERSATA NUMBER(12,2),
UTENTE            VARCHAR2(8),
TIPO_AGG          VARCHAR2(1),
DATA_AGG          DATE
);

CREATE UNIQUE INDEX DEGL_PK ON DENUNCIA_GLA
(ANNO,GESTIONE,CI)
;